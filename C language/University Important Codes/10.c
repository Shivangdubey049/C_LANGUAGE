//Wacp to show the working of relational operators

#include<stdio.h>
void main(){
    int a = 10,b = 20;
    printf("\n Output for > operator is %d",a>b);
    printf("\n Output for < operator is %d",a<b);
    printf("\n Output for >= operator is %d",a>=b);
    printf("\n Output for <= operator is %d",a<=b);
    printf("\n Output for == operator is %d",a==b);
    printf("\n Output for != operator is %d",a!=b);
}

// 0 in the output means false and 1 means true