//Wacp to print and show working of bitwise operator
#include<stdio.h>
void main(){
    int a = 10,b=15;
    printf("\n& operator on a and b gives : %d ",a&b);
    printf("\n| operator on a and b gives : %d ",a|b);
    printf("\n^ operator on a and b gives : %d ",a^b);
    printf("\n<< operator on a and b gives : %d ",a<<1);
    printf("\n>> operator on a and b gives : %d ",a>>1);
}
