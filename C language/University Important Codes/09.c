//Write a program to print and show working of arithmetic operator

#include<stdio.h>
void main(){
    int a = 20 , b = 10;
    printf("\n + operator output is %d", a+b);
    printf("\n - operator output is %d", a - b);
    printf("\n * operator output is %d", a * b);
    printf("\n / operator output is %d", a / b);
    printf("\n % operator output is %d", a % b);
}